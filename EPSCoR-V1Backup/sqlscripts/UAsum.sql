	SUM(Fnode),
	SUM(Tnode),
	SUM(LCOVV11),
	SUM(LCOVV21),
	SUM(LCOVV22),
	SUM(LCOVV23),
	SUM(LCOVV24),
	SUM(LCOVV31),
	SUM(LCOVV41),
